package com.example.movieapplication;

public class ListModel {

    private String name = "";
    private int age;

    public void set_name(String name) {
        this.name = name;
    }

    public void set_age(int age) {
        this.age = age;
    }

    public String get_name() {
        return this.name;
    }

    public int get_age() {
        return this.age;
    }

}
